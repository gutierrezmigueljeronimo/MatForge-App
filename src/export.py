"""
src/export.py
Export predicted PBR maps to engine‑specific PNG files and package them
into a downloadable ZIP archive.

Pure logic – only NumPy, Pillow, zipfile, io – no Streamlit, no GPU, no state.
"""

import io
import zipfile

import numpy as np
from PIL import Image


# ---------------------------------------------------------------------------
# Engine‑specific constants and valid names
# ---------------------------------------------------------------------------
